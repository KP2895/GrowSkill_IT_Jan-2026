package day3_loops;

public class Practice_Forloops {

	public static void main(String[] args) {


		//Print from 1 to 10
		
		int a;
		for (a=1;a<=10;a++) {
			
			System.out.print(a+ " ");
		}
		
		System.out.println("\n----------------------");
		
		int b;
		for (b=10;b>=1;b--) {
			
			System.out.print(b+ " ");
		}
		
		

	}

}
